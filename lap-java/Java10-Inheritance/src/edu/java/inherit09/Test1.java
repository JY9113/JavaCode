package edu.java.inherit09;

public class Test1 {
	private int a;
	int b;
	protected int c;
	public int d;
	// 생성자 만들지 않으면 디폴트 생성자를 자동으로 만들어줌
	
	public void display() {
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		System.out.println("d = " + d);
		
	}
	

}
