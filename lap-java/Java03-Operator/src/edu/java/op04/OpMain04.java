package edu.java.op04;

public class OpMain04 {

	public static void main(String[] args) {
		System.out.println("비교 연산자(>, <, >=, <=, ==, !=) \n");
		//비교 연산자의 연산 결과는 true 또는 false
		
		
		System.out.println(10 > 20);
		System.out.println(10 < 20);
		System.out.println(123 == 100);
		System.out.println(123 != 100);

	}

}
